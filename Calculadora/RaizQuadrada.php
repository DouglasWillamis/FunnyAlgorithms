<?php


namespace Douglas\Hacktoberfest2020;


/**
 * Class RaizQuadrada
 * @package Douglas\Hacktoberfest2020
 */
class RaizQuadrada
{
    /**
     * @param int $radicando
     * @return float
     */
    public static function calcular(int $radicando): float
    {
        return sqrt($radicando);
    }
}
